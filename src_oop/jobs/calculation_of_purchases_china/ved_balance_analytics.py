    @staticmethod
    def make_unique_column_names(column_names: list[str]) -> list[str]:
        """
        Р”РµР»Р°РµС‚ РЅР°Р·РІР°РЅРёСЏ РєРѕР»РѕРЅРѕРє СѓРЅРёРєР°Р»СЊРЅС‹РјРё, РµСЃР»Рё РІ С‚Р°Р±Р»РёС†Рµ РІСЃС‚СЂРµС‚РёР»РёСЃСЊ РґСѓР±Р»Рё.

        РџР°СЂР°РјРµС‚СЂС‹:
            column_names:
                РЎРїРёСЃРѕРє СѓР¶Рµ РЅРѕСЂРјР°Р»РёР·РѕРІР°РЅРЅС‹С… Р·Р°РіРѕР»РѕРІРєРѕРІ.

        Р’РѕР·РІСЂР°С‰Р°РµС‚:
            РќРѕРІС‹Р№ СЃРїРёСЃРѕРє Р·Р°РіРѕР»РѕРІРєРѕРІ, РіРґРµ РїРѕРІС‚РѕСЂС‹ РїРѕР»СѓС‡Р°СЋС‚ С‡РёСЃР»РѕРІРѕР№ СЃСѓС„С„РёРєСЃ.

        РљСЂР°Р№РЅРёР№ СЃР»СѓС‡Р°Р№:
            РµСЃР»Рё РІ Google Sheets РѕРґРЅР° Рё С‚Р° Р¶Рµ РїРѕРґРїРёСЃСЊ РІСЃС‚СЂРµС‡Р°РµС‚СЃСЏ РЅРµСЃРєРѕР»СЊРєРѕ СЂР°Р·,
            `pandas.DataFrame` Р±РµР· С‚Р°РєРѕР№ РѕР±СЂР°Р±РѕС‚РєРё Р±СѓРґРµС‚ СЂР°Р±РѕС‚Р°С‚СЊ РЅРµСѓСЃС‚РѕР№С‡РёРІРѕ.
        """
        seen_columns: dict[str, int] = {}
        unique_columns: list[str] = []

