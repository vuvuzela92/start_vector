    def build_balance_dataframe(self, df_source: pd.DataFrame) -> pd.DataFrame:
        """
        РЎРѕР±РёСЂР°РµС‚ РёС‚РѕРіРѕРІС‹Р№ `ved_balance_df` РїРѕ РІСЃРµРј СЌС‚Р°РїР°Рј РёР· `VED_PAYMENT_CONFIGS`.

        РџР°СЂР°РјРµС‚СЂС‹:
            df_source:
                РСЃС…РѕРґРЅС‹Р№ DataFrame Р’Р­Р” РІ С€РёСЂРѕРєРѕРј С„РѕСЂРјР°С‚Рµ.

        Р’РѕР·РІСЂР°С‰Р°РµС‚:
            DataFrame РІ СѓР·РєРѕРј С„РѕСЂРјР°С‚Рµ, РіРґРµ РєР°Р¶РґР°СЏ СЃС‚СЂРѕРєР° вЂ” СЌС‚Рѕ РѕС‚РґРµР»СЊРЅС‹Р№ СЌС‚Р°Рї РїР»Р°С‚РµР¶Р°.

        Р’Р°Р¶РЅС‹Рµ Р±РёР·РЅРµСЃ-РїСЂР°РІРёР»Р°:
        - РёР· РѕРґРЅРѕР№ СЃС‚СЂРѕРєРё РёСЃС…РѕРґРЅРѕР№ С‚Р°Р±Р»РёС†С‹ РјРѕР¶РµС‚ РїРѕР»СѓС‡РёС‚СЊСЃСЏ РЅРµСЃРєРѕР»СЊРєРѕ СЃС‚СЂРѕРє СЂРµР·СѓР»СЊС‚Р°С‚Р°;
        - РїСѓСЃС‚С‹Рµ СЌС‚Р°РїС‹ Р±РµР· СЃС‚Р°С‚СѓСЃР° РѕС‚Р±СЂР°СЃС‹РІР°СЋС‚СЃСЏ;
        - РїРѕСЂСЏРґРѕРє СЃС‚СЂРѕРє СЃРѕС…СЂР°РЅСЏРµС‚СЃСЏ СЃС‚Р°Р±РёР»СЊРЅС‹Рј, С‡С‚РѕР±С‹ РїСЂРѕС‰Рµ Р±С‹Р»Рѕ СЃРІРµСЂСЏС‚СЊ СЂРµР·СѓР»СЊС‚Р°С‚
          СЃ РёСЃС…РѕРґРЅРѕР№ С‚Р°Р±Р»РёС†РµР№ РїСЂРё РѕС‚Р»Р°РґРєРµ.
        """
        df_base = self.prepare_source_dataframe(df_source)

        # Р—РґРµСЃСЊ РїСЂРѕРёСЃС…РѕРґРёС‚ РєР»СЋС‡РµРІРѕРµ РїСЂРµРѕР±СЂР°Р·РѕРІР°РЅРёРµ VED-С‚Р°Р±Р»РёС†С‹:
        # С€РёСЂРѕРєР°СЏ СЃС‚СЂРѕРєР° РїРѕСЃС‚Р°РІРєРё РїСЂРµРІСЂР°С‰Р°РµС‚СЃСЏ РІ РЅРµСЃРєРѕР»СЊРєРѕ СЃС‚СЂРѕРє РїР»Р°С‚РµР¶РЅРѕРіРѕ Р±Р°Р»Р°РЅСЃР°.
        payment_frames = [
            self.build_payment_dataframe(
                df_source=df_source,
                df_base=df_base,
                payment_config=payment_config,
            )
            for payment_config in VED_PAYMENT_CONFIGS
        ]

        df_balance = pd.concat(payment_frames, ignore_index=True)

        # Р’ РёС‚РѕРіРѕРІС‹Р№ Р±Р°Р»Р°РЅСЃ Р±РµСЂРµРј С‚РѕР»СЊРєРѕ С‚Рµ СЃС‚СЂРѕРєРё, РіРґРµ СЌС‚Р°Рї РґРµР№СЃС‚РІРёС‚РµР»СЊРЅРѕ Р·Р°РїРѕР»РЅРµРЅ.
        # РџСѓСЃС‚РѕР№ СЃС‚Р°С‚СѓСЃ РїРѕС‡С‚Рё РІСЃРµРіРґР° РѕР·РЅР°С‡Р°РµС‚, С‡С‚Рѕ РєРѕРЅРєСЂРµС‚РЅС‹Р№ РІРёРґ РїР»Р°С‚РµР¶Р°
        # РґР»СЏ СЌС‚РѕР№ РїРѕСЃС‚Р°РІРєРё РѕС‚СЃСѓС‚СЃС‚РІСѓРµС‚.
        meaningful_mask = (
            df_balance["Сумма_оплаты"].notna()
            | df_balance["Дата_платеж_календарь"].notna()
            | df_balance["Статус_по_этапу"].fillna("").astype(str).str.strip().ne("")
        )
        df_balance = df_balance[meaningful_mask].copy()

        return df_balance.sort_values(
            by=[
                "№ проформы в документах и 1С",
                "_Порядок исходной строки",
                "Номер этапа платежа",
            ],
            kind="stable",
        ).drop(columns="_Порядок исходной строки").reset_index(drop=True)

    @staticmethod
    def add_payment_status_amounts(df_balance: pd.DataFrame) -> pd.DataFrame:
        """
        Р”РµР»РёС‚ СЃСѓРјРјСѓ РїР»Р°С‚РµР¶Р° РЅР° РѕРїР»Р°С‡РµРЅРЅС‹Р№ Рё РЅРµРѕРїР»Р°С‡РµРЅРЅС‹Р№ РѕСЃС‚Р°С‚РѕРє РїРѕ СЃС‚Р°С‚СѓСЃСѓ СЌС‚Р°РїР°.

        РџР°СЂР°РјРµС‚СЂС‹:
            df_balance:
                DataFrame СЃ СѓР¶Рµ СЃС„РѕСЂРјРёСЂРѕРІР°РЅРЅС‹РјРё СЃС‚СЂРѕРєР°РјРё РїР»Р°С‚РµР¶РЅС‹С… СЌС‚Р°РїРѕРІ.

        Р’РѕР·РІСЂР°С‰Р°РµС‚:
            DataFrame СЃ РґРѕРїРѕР»РЅРёС‚РµР»СЊРЅС‹РјРё РєРѕР»РѕРЅРєР°РјРё `РћРїР»Р°С‡РµРЅРѕ` Рё `РќРµ_РѕРїР»Р°С‡РµРЅРѕ`.

        Р‘РёР·РЅРµСЃ-РїСЂР°РІРёР»Рѕ:
            РµСЃР»Рё `РЎС‚Р°С‚СѓСЃ_РїРѕ_СЌС‚Р°РїСѓ == "РѕРїР»Р°С‡РµРЅРѕ"`, РІСЃСЏ СЃСѓРјРјР° СЌС‚Р°РїР° РёРґРµС‚ РІ РєРѕР»РѕРЅРєСѓ
            `РћРїР»Р°С‡РµРЅРѕ`, РёРЅР°С‡Рµ вЂ” РІ `РќРµ_РѕРїР»Р°С‡РµРЅРѕ`.
        """
        df_result = df_balance.copy()
        paid_mask = (
            df_result["Статус_по_этапу"]
            .fillna("")
            .astype(str)
            .str.strip()
            .str.lower()
            .eq("оплачено")
        )
        payment_amount = pd.to_numeric(df_result["Сумма_оплаты"], errors="coerce").fillna(0)

        df_result["Оплачено"] = payment_amount.where(paid_mask, 0)
        df_result["Не_оплачено"] = payment_amount.where(~paid_mask, 0)
        return df_result

